package com.sjsu.etcd.bean;

public class ServiceObject {

	private String strAppName;
	private String strHost;
	private String strPort;
	public String getStrAppName() {
		return strAppName;
	}
	public void setStrAppName(String strAppName) {
		this.strAppName = strAppName;
	}
	public String getStrHost() {
		return strHost;
	}
	public void setStrHost(String strHost) {
		this.strHost = strHost;
	}
	public String getStrPort() {
		return strPort;
	}
	public void setStrPort(String strPort) {
		this.strPort = strPort;
	}
}
